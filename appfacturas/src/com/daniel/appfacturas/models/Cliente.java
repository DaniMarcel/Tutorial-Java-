package com.daniel.appfacturas.models;

public class Cliente {
    // ATRIBUTOS
    private String nombre;
    private String nif;

    // GETTERS
    public String getNombre() {
        return nombre;
    }
    public String getNif() {
        return nif;
    }

    // SETTERS
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public void setNif(String nif) {
        this.nif = nif;
    }
}
